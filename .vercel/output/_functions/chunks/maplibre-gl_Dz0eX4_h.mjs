	acidityType = "sweet";
	sweetnessIntensity = "medium";
	mouthfeel = "medium";
	mouthfeelTypes = [];
	mood = "🙂";
	shopSearchName = "";
	lat = null;
	lng = null;
	shopId = null;
	isLocationSelected = false;

