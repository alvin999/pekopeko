class ToastStore {
	message = "";
	type = "warning";
	visible = false;
	timer = null;

	show(msg, t = "warning") {
		if (this.timer) clearTimeout(this.timer);

		this.message = msg;
		this.type = t;
		this.visible = true;

		this.timer = setTimeout(
			() => {
				this.visible = false;
			},
			3e3
		);
	}
}

